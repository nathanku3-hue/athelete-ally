RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# ??Node??
echo -e "${BLUE}?? ??????...${NC}"
if ! command -v node &> /dev/null; then
    echo -e "${RED}? Node.js ???${NC}"
    exit 1
fi

NODE_VERSION=$(node --version)
echo -e "${GREEN}? Node.js ??: $NODE_VERSION${NC}"

# ??Docker
if ! command -v docker &> /dev/null; then
    echo -e "${RED}? Docker ???${NC}"
    exit 1
fi

if ! command -v docker-compose &> /dev/null; then
    echo -e "${RED}? Docker Compose ???${NC}"
    exit 1
fi

echo -e "${GREEN}? Docker ????${NC}"

# 1. ??????
echo -e "${BLUE}?? ??????...${NC}"
npm run check-ports || {
    echo -e "${YELLOW}??  ???????????...${NC}"
}

# 2. ????
echo -e "${BLUE}?? ??????...${NC}"
npm install

# 3. ????????
echo -e "${BLUE}?? ????????...${NC}"
docker-compose -f docker/compose/preview.yml up -d postgres redis nats

# 4. ????????
echo -e "${BLUE}? ????????...${NC}"
sleep 15

# ??PostgreSQL??
echo -e "${BLUE}?? ???????...${NC}"
until docker-compose -f docker/compose/preview.yml exec postgres pg_isready -U athlete -d athlete; do
    echo -e "${YELLOW}? ??PostgreSQL??...${NC}"
    sleep 2
done
echo -e "${GREEN}? PostgreSQL ??${NC}"

# ??Redis??
echo -e "${BLUE}?? ??Redis??...${NC}"
until docker-compose -f docker/compose/preview.yml exec redis redis-cli ping; do
    echo -e "${YELLOW}? ??Redis??...${NC}"
    sleep 2
done
echo -e "${GREEN}? Redis ??${NC}"

# 5. ??Prisma???
